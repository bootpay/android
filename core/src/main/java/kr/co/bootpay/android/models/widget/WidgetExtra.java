package kr.co.bootpay.android.models.widget;

public class WidgetExtra {
    private String directCardCompany;
    private String directCardQuota;
    private String cardQuota;

    public WidgetExtra() {}

    public String getDirectCardCompany() {
        return directCardCompany;
    }

    public void setDirectCardCompany(String directCardCompany) {
        this.directCardCompany = directCardCompany;
    }

    public String getDirectCardQuota() {
        return directCardQuota;
    }

    public void setDirectCardQuota(String directCardQuota) {
        this.directCardQuota = directCardQuota;
    }

    public String getCardQuota() {
        return cardQuota;
    }

    public void setCardQuota(String cardQuota) {
        this.cardQuota = cardQuota;
    }
}
